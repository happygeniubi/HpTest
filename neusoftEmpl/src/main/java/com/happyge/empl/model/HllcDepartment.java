package com.happyge.empl.model;


public class HllcDepartment {

  private String deId;
  private String deName;
  private String deDate;
  private String dePeople;
  private String deMessage;


  public String getDeId() {
    return deId;
  }

  public void setDeId(String deId) {
    this.deId = deId;
  }


  public String getDeName() {
    return deName;
  }

  public void setDeName(String deName) {
    this.deName = deName;
  }


  public String getDeDate() {
    return deDate;
  }

  public void setDeDate(String deDate) {
    this.deDate = deDate;
  }


  public String getDePeople() {
    return dePeople;
  }

  public void setDePeople(String dePeople) {
    this.dePeople = dePeople;
  }


  public String getDeMessage() {
    return deMessage;
  }

  public void setDeMessage(String deMessage) {
    this.deMessage = deMessage;
  }

}
